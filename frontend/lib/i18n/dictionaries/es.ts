// internally, but other locales (en/pt/zh) need to supply their own text for
// every key — not the literal Spanish string. Widen every leaf to `string`
// while preserving the exact namespace/key shape, so `Dictionary` only
// enforces structural key parity across locales.
type WidenStrings<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? WidenStrings<U>[]
    : { -readonly [K in keyof T]: WidenStrings<T[K]> };

